def somma():
    n1 = int(input("Inserisci il primo numero: "))
    n2 = int(input("Inserisci il secondo numero: "))
    print(f"{n1} + {n2} = {n1+n2}")

def sottrazione():
    n1 = int(input("Inserisci il primo numero: "))
    n2 = int(input("Inserisci il secondo numero: "))
    print(f"{n1} - {n2} = {n1-n2}")

